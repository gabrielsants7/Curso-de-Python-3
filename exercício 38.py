primeiro = int(input('Primeiro número: '))
segundo = int(input('Segundo número: '))
if primeiro > segundo:
    print('O PRIMEIRO valor é maior')
elif primeiro == segundo:
    print('Os dois valores são IGUAIS')
else:
    print('O SEGUNDO valor é maior')

